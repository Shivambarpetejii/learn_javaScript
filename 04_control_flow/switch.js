

// switch statement 

let month = 14;

switch (month) {
    case 1:
        console.log(` jan`);
        break;

    case 2:
        console.log(`fab`);
        break;
    case 3:
        console.log(`mar`);
        break;
    case 4:
        console.log(`apr`);
        break;

    default:
        console.log(`no!!`);
        break;
}